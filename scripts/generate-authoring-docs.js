const fs = require('fs');
const path = require('path');

const repoRoot = path.resolve(__dirname, '..');
const specPath = path.join(repoRoot, 'spec', 'authoring-spec.json');
const outputPath = path.join(repoRoot, 'docs', 'authoring.md');

function readJson(filePath) {
  return JSON.parse(fs.readFileSync(filePath, 'utf8'));
}

function formatList(items, formatter = (item) => item) {
  return items.map((item) => `- ${formatter(item)}`).join('\n');
}

function renderRefs(title, refs, formatter) {
  if (!refs || refs.length === 0) return '';
  return `\n**${title}**\n${formatList(refs, formatter)}\n`;
}

function renderRule(rule) {
  const lines = [
    `### ${rule.id}`,
    `- **Level**: ${rule.level}`,
    `- **Status**: ${rule.status}`,
    `- **Scope**: ${rule.scope.join(', ')}`,
    `- **Rule**: ${rule.rule}`,
    `- **Why**: ${rule.why}`
  ];

  if (rule.enforcement?.length) {
    lines.push(`- **Enforced by**: ${rule.enforcement.join(', ')}`);
  }

  if (rule.checks?.length) {
    lines.push('', '**Checks**', formatList(rule.checks));
  }

  if (rule.antiPatterns?.length) {
    lines.push('', '**Anti-patterns**', formatList(rule.antiPatterns));
  }

  if (rule.examples?.length) {
    lines.push('', '**Examples**', formatList(rule.examples, (example) => `\`${example}\``));
  }

  if (rule.exampleRefs?.length) {
    lines.push(
      '',
      '**Example refs**',
      formatList(rule.exampleRefs, (ref) => {
        const note = ref.note ? ` — ${ref.note}` : '';
        return `\`${ref.path}\`${note}`;
      })
    );
  }

  if (rule.sourceRefs?.length) {
    lines.push(
      '',
      '**Source refs**',
      formatList(rule.sourceRefs, (ref) => {
        const note = ref.note ? ` — ${ref.note}` : '';
        return `\`${ref.path}\` (${ref.kind})${note}`;
      })
    );
  }

  return `${lines.join('\n')}\n`;
}

function renderTopic(topic) {
  const parts = [`## ${topic.title}`];
  for (const rule of topic.rules) {
    parts.push(renderRule(rule));
  }
  return parts.join('\n');
}

function buildMarkdown(spec) {
  const authoringJourney = spec.topics.find((topic) => topic.id === 'authoring-journey');
  const currentTopics = spec.topics.filter((topic) => topic.id !== 'authoring-journey');

  const parts = [
    '# Workflow Authoring Guide',
    '',
    '> Generated from `spec/authoring-spec.json`. Do not edit this file by hand.',
    '',
    spec.purpose,
    '',
    '## Principles',
    formatList(spec.principles),
    '',
    '## Consumers',
    formatList(spec.consumedBy, (consumer) => `\`${consumer}\``),
    '',
    '## Change protocol',
    formatList(spec.changeProtocol)
  ];

  if (authoringJourney) {
    parts.push('', '## Quickstart');
    for (const rule of authoringJourney.rules) {
      parts.push(renderRule(rule));
    }
  }

  parts.push('', '## Current authoring rules');
  for (const topic of currentTopics) {
    parts.push('', renderTopic(topic));
  }

  if ((spec.plannedTopics && spec.plannedTopics.length > 0) || (spec.plannedRules && spec.plannedRules.length > 0)) {
    parts.push('', '## Planned guidance', '', '> This section is future-facing. It is **not** current runtime truth or current engine support.');

    for (const topic of spec.plannedTopics ?? []) {
      parts.push('', renderTopic(topic));
    }

    if (spec.plannedRules?.length) {
      parts.push('', '## Planned standalone rules');
      for (const rule of spec.plannedRules) {
        parts.push(renderRule(rule));
      }
    }
  }

  parts.push('', '## Scope catalog', formatList(spec.scopeCatalog, (entry) => `\`${entry.id}\`: ${entry.description}`), '');

  return `${parts.join('\n')}\n`;
}

function main() {
  const checkMode = process.argv.includes('--check');
  const spec = readJson(specPath);
  const markdown = buildMarkdown(spec);

  if (checkMode) {
    const existing = fs.existsSync(outputPath) ? fs.readFileSync(outputPath, 'utf8') : '';
    if (existing !== markdown) {
      console.error('docs/authoring.md is out of date. Run `npm run docs:authoring`.');
      process.exit(1);
    }
    console.log('authoring docs are up to date');
    return;
  }

  fs.writeFileSync(outputPath, markdown, 'utf8');
  console.log(`Generated ${path.relative(repoRoot, outputPath)}`);
}

main();
